/* CONDITIONAL OPERATORS
 A shorthand “if – else” can be written
 using the conditional or ternary operators 
 condition ? expression-if-true : expression-if-false  .*/
#include<stdio.h>

int main(){
    
    // condition ? expression-if-true : expression-if-false 

    int a = 345 , b = 34;

    a > b ? printf("a is greater than b") : printf("a is lesser than b");
     
    

    return 0;
}
 